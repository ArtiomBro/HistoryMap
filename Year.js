function createEventButtons(year) {
  const container = document.querySelector('.map-container');
  const events = eventsData[year] || [];

  document.querySelectorAll('.event-button').forEach(btn => btn.remove());

  events.forEach(event => {
    const button = document.createElement('div');
    button.className = 'event-button';
    button.textContent = ' ';
    button.style.left = `${event.coordinates.x}%`;
    button.style.top = `${event.coordinates.y}%`;
    button.dataset.eventId = event.id;
    button.addEventListener('click', () => showEventDetails(event));
    container.appendChild(button);
  });
}

function showEventDetails(event) {
  const modal = document.querySelector('.modal-overlay');
  const detailsContainer = document.getElementById('event-details');

  if (!event) return;

  detailsContainer.innerHTML = `
    <h2>${event.name}</h2>
    <div class="event-layout">
      <div class="event-description-block">
        <div class="event-center-block">
          <div class="event-image">
            <img class="event-image-img" src="${event.picture || ''}" alt="${event.name}">
          </div>
          <div class="event-citate">${event.citate}</div>
        </div>
        <div>
          <div class="event-location">${event.location}</div>
          <div class="event-description">${event.description}</div>
        </div>
      </div>

      <div class="event-section">
        <h3>Details</h3>
        <ul>
          <li><strong>Value:</strong> ${event.value}</li>
          <li><strong>Outcome:</strong> ${event.outcome}</li>
        </ul>
      </div>
    </div>
  `;

  modal.classList.add('active');
}

function closeModal() {
  document.querySelector('.modal-overlay').classList.remove('active');
}

document.addEventListener('DOMContentLoaded', function () {
  const yearSelect = document.getElementById('year-select');

  yearSelect.addEventListener('change', function () {
    createEventButtons(this.value);
  });

  document.querySelector('.close-modal').addEventListener('click', closeModal);

  document.querySelector('.modal-overlay').addEventListener('click', function (e) {
    if (e.target === this) closeModal();
  });

  createEventButtons(yearSelect.value);
});
